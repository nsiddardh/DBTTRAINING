

SELECT DISTINCT DW_DAYPART, FIRST_VALUE(DAYPARTBGNTM) over(partition by DW_DAYPART order by DW_DAYPART) 
AS DAYPART_BGN_TM , FIRST_VALUE(DAYPARTENDTM) over(partition by DW_DAYPART order by DW_DAYPART) AS DAYPART_END_TM
FROM TIME_DAYPART_DET
WHERE 
DAYPARTNAME <> 'OFF PREM'
AND  DW_DAYPART IN (1,2,3,4,5,6)